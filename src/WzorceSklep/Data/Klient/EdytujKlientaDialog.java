    private void skasujButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_skasujButtonActionPerformed
        if(showConfirmationDialog(this, klient.getName())==JOptionPane.YES_OPTION)
            try {
                new DAOFactory().getKlientDAO().delete(klient.getID());
                dispose();
            } catch (SQLException e) {
                e.printStackTrace();  //To change body of catch statement use File | Settings | File Templates.
                Util.showErrorDialog(this,e);
            }
    }//GEN-LAST:event_skasujButtonActionPerformed

    private static int showConfirmationDialog(Component owner, String name) {
        final String[] options = { "Tak", "Nie" };
        return JOptionPane.showOptionDialog(owner,
                String.format("Czy na pewno chcesz usunąć %s?", name),
                "Potwierdzenie usunięcia",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE,
                null, options, options[1]);
    }

    private boolean nieWypelnione() {
        return  nazwiskoField.getText().equals(NAZWISKO_PLACEHOLDER) ||
                !kodPocztowyField.isEditValid() ||
                miejscowoscField.getText().equals(MIEJSCOWOSC_PLACEHOLDER) ||
                pocztaField.getText().equals(POCZTA_PLACEHOLDER) ||
                krajField.getText().equals(KRAJ_PLACEHOLDER);
    }

