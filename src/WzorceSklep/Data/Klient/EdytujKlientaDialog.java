    private Klient InitKlient(DAOFactory daoFactory, int idKlienta) {
        Klient wynik = new Klient();

        try {
            wynik = daoFactory.getKlientDAO().getById(idKlienta);
            imieField.setText(wynik.getImie());
            nazwiskoField.setText(wynik.getNazwisko());
            mailField.setText(wynik.getMail());
            telefonField.setText(wynik.getTelefon());

            AdresOsoby adres = wynik.getAdres();
            ulicaField.setText(adres.getUlica());
            kodPocztowyField.setText(adres.getKodPocztowy());
            pocztaField.setText(adres.getPoczta());
            miejscowoscField.setText(adres.getMiejscowosc());
            krajField.setText(adres.getKraj());
            String nrDomuText = (adres.getNrDomu() == null) ? "" : adres.getNrDomu().toString();
            nrdomuField.setText(nrDomuText);
            String nrLokaluText = (adres.getNrLokalu() == null) ? "" : adres.getNrLokalu().toString();
            nrlokaluField.setText(nrLokaluText);


        } catch (SQLException e) {
            e.printStackTrace();  //To change body of catch statement use File | Settings | File Templates.
        }

        return wynik;
    }

